@Injectable()
export class I18nService {
  t(lang: string | null, key: string, ...args: any[]): string {
    const L = lang === 'ky' ? 'ky' : 'ru';
    const entry = T[key];
    if (!entry) return key;
    const val = entry[L] !== undefined ? entry[L] : entry.ru;
    return typeof val === 'function' ? val(...args) : val;
  }
}
